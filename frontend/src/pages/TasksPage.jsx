import { useState, useEffect } from 'react'
import { Link } from 'react-router-dom'
import { useAuth } from '../hooks/useAuth'
import api from '../services/api'
import BidModal from '../components/BidModal'
import UserAvatar from '../components/UserAvatar'

const TasksPage = () => {
  const { user } = useAuth()
  
  const [tasks, setTasks] = useState([])
  const [loading, setLoading] = useState(true)
  const [error, setError] = useState('')
  const [bidModalOpen, setBidModalOpen] = useState(false)
  const [selectedTask, setSelectedTask] = useState(null)
  const [filters, setFilters] = useState({
    category: '',
    status: '',
    search: ''
  })
  const [pagination, setPagination] = useState({
    page: 1,
    limit: 10,
    total: 0,
    totalPages: 0
  })

  const categoryLabels = {
    flat: '–ö–≤–∞—Ä—Ç–∏—Ä–Ω—ã–π –ø–µ—Ä–µ–µ–∑–¥',
    office: '–û—Ñ–∏—Å–Ω—ã–π –ø–µ—Ä–µ–µ–∑–¥',
    intercity: '–ú–µ–∂–≥–æ—Ä–æ–¥—Å–∫–∏–π –ø–µ—Ä–µ–µ–∑–¥',
    garbage: '–í—ã–≤–æ–∑ –º—É—Å–æ—Ä–∞'
  }

  const statusLabels = {
    draft: '–ß–µ—Ä–Ω–æ–≤–∏–∫',
    active: '–ê–∫—Ç–∏–≤–Ω–∞—è',
    in_progress: '–í –ø—Ä–æ—Ü–µ—Å—Å–µ',
    completed: '–ó–∞–≤–µ—Ä—à–µ–Ω–∞'
  }

  const statusColors = {
    draft: 'status-draft',
    active: 'status-active',
    in_progress: 'status-pending',
    completed: 'status-completed'
  }

  useEffect(() => {
    loadTasks()
  }, [filters, pagination.page])

  const loadTasks = async () => {
    try {
      setLoading(true)
      
      const params = {
        page: pagination.page,
        limit: pagination.limit,
        ...filters
      }
      
      // –£–¥–∞–ª—è–µ–º –ø—É—Å—Ç—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
      Object.keys(params).forEach(key => {
        if (!params[key]) delete params[key]
      })
      
      console.log('–ó–∞–≥—Ä—É–∂–∞—é –∑–∞—è–≤–∫–∏ —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏:', params)
      
      const response = await api.get('/tasks', { params })
      console.log('–û—Ç–≤–µ—Ç API:', response.data)
      
      if (response.data.success) {
        setTasks(response.data.tasks)
        setPagination(prev => ({
          ...prev,
          ...response.data.pagination
        }))
      } else {
        setError('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∑–∞—è–≤–æ–∫')
      }
    } catch (err) {
      console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∑–∞—è–≤–æ–∫:', err)
      setError(err.response?.data?.message || '–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∑–∞—è–≤–æ–∫')
    } finally {
      setLoading(false)
    }
  }

  const handleFilterChange = (key, value) => {
    setFilters(prev => ({
      ...prev,
      [key]: value
    }))
    setPagination(prev => ({
      ...prev,
      page: 1
    }))
  }

  const handlePageChange = (page) => {
    setPagination(prev => ({
      ...prev,
      page
    }))
  }

  const formatDate = (dateString) => {
    return new Date(dateString).toLocaleDateString('ru-RU', {
      year: 'numeric',
      month: 'short',
      day: 'numeric'
    })
  }

  const formatDateTime = (dateString) => {
    return new Date(dateString).toLocaleString('ru-RU', {
      month: 'short',
      day: 'numeric',
      hour: '2-digit',
      minute: '2-digit'
    })
  }

  const handleBidClick = (task) => {
    setSelectedTask(task)
    setBidModalOpen(true)
  }

  const handleBidCreated = (newBid) => {
    console.log('–ù–æ–≤—ã–π –æ—Ç–∫–ª–∏–∫ —Å–æ–∑–¥–∞–Ω:', newBid)
    // –û–±–Ω–æ–≤–ª—è–µ–º –∑–∞—è–≤–∫—É –≤ —Å–ø–∏—Å–∫–µ - –¥–æ–±–∞–≤–ª—è–µ–º –æ—Ç–∫–ª–∏–∫ –∏ –ø–æ–º–µ—á–∞–µ–º –∫–∞–∫ userBid
    setTasks(prev => prev.map(task => 
      task.id === selectedTask.id 
        ? { 
            ...task, 
            bids: [...(task.bids || []), newBid],
            userBid: newBid // –î–æ–±–∞–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ç–æ–º, —á—Ç–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –æ—Ç–∫–ª–∏–∫–Ω—É–ª—Å—è
          }
        : task
    ))
    setBidModalOpen(false)
    setSelectedTask(null)
  }

  return (
    <div className="max-w-6xl mx-auto">
      {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ –∏ –∫–Ω–æ–ø–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è */}
      <div className="flex justify-between items-center mb-6">
        <h1 className="text-3xl font-bold">–ó–∞—è–≤–∫–∏ –Ω–∞ –ø–µ—Ä–µ–µ–∑–¥</h1>
        {user && (
          <Link to="/create-task" className="btn btn-primary">
            + –°–æ–∑–¥–∞—Ç—å –∑–∞—è–≤–∫—É
          </Link>
        )}
      </div>

      {/* –§–∏–ª—å—Ç—Ä—ã */}
      <div className="card mb-6">
        <h3 className="font-semibold mb-4">üîç –§–∏–ª—å—Ç—Ä—ã</h3>
        <div className="grid grid-cols-1 md:grid-cols-4 gap-4">
          {/* –ü–æ–∏—Å–∫ */}
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              –ü–æ–∏—Å–∫
            </label>
            <input
              type="text"
              placeholder="–ü–æ–∏—Å–∫ –ø–æ –∑–∞–≥–æ–ª–æ–≤–∫—É..."
              value={filters.search}
              onChange={(e) => handleFilterChange('search', e.target.value)}
              className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
            />
          </div>

          {/* –ö–∞—Ç–µ–≥–æ—Ä–∏—è */}
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              –¢–∏–ø –ø–µ—Ä–µ–µ–∑–¥–∞
            </label>
            <select
              value={filters.category}
              onChange={(e) => handleFilterChange('category', e.target.value)}
              className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
            >
              <option value="">–í—Å–µ —Ç–∏–ø—ã</option>
              <option value="flat">–ö–≤–∞—Ä—Ç–∏—Ä–Ω—ã–π</option>
              <option value="office">–û—Ñ–∏—Å–Ω—ã–π</option>
              <option value="intercity">–ú–µ–∂–≥–æ—Ä–æ–¥—Å–∫–∏–π</option>
              <option value="garbage">–í—ã–≤–æ–∑ –º—É—Å–æ—Ä–∞</option>
            </select>
          </div>

          {/* –°—Ç–∞—Ç—É—Å */}
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              –°—Ç–∞—Ç—É—Å
            </label>
            <select
              value={filters.status}
              onChange={(e) => handleFilterChange('status', e.target.value)}
              className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
            >
              <option value="">–í—Å–µ —Å—Ç–∞—Ç—É—Å—ã</option>
              <option value="active">–ê–∫—Ç–∏–≤–Ω—ã–µ</option>
              <option value="in_progress">–í –ø—Ä–æ—Ü–µ—Å—Å–µ</option>
              <option value="completed">–ó–∞–≤–µ—Ä—à–µ–Ω–Ω—ã–µ</option>
            </select>
          </div>

          {/* –°–±—Ä–æ—Å —Ñ–∏–ª—å—Ç—Ä–æ–≤ */}
          <div className="flex items-end">
            <button
              onClick={() => {
                setFilters({ category: '', status: '', search: '' })
                setPagination(prev => ({ ...prev, page: 1 }))
              }}
              className="btn btn-secondary w-full"
            >
              –°–±—Ä–æ—Å–∏—Ç—å
            </button>
          </div>
        </div>
      </div>

      {/* –°–ø–∏—Å–æ–∫ –∑–∞—è–≤–æ–∫ */}
      {loading ? (
        <div className="flex justify-center items-center py-12">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-blue-600"></div>
        </div>
      ) : error ? (
        <div className="text-center py-12">
          <div className="text-red-600 text-xl mb-4">{error}</div>
          <button onClick={loadTasks} className="btn btn-primary">
            –ü–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å –µ—â–µ —Ä–∞–∑
          </button>
        </div>
      ) : tasks.length === 0 ? (
        <div className="text-center py-12">
          <div className="text-4xl mb-4">üì¶</div>
          <div className="text-xl text-gray-600 mb-2">–ó–∞—è–≤–æ–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ</div>
          <div className="text-gray-500 mb-4">
            {Object.values(filters).some(v => v) 
              ? '–ü–æ–ø—Ä–æ–±—É–π—Ç–µ –∏–∑–º–µ–Ω–∏—Ç—å —Ñ–∏–ª—å—Ç—Ä—ã –ø–æ–∏—Å–∫–∞'
              : '–°—Ç–∞–Ω—å—Ç–µ –ø–µ—Ä–≤—ã–º, –∫—Ç–æ —Å–æ–∑–¥–∞—Å—Ç –∑–∞—è–≤–∫—É!'
            }
          </div>
          {user && (
            <Link to="/create-task" className="btn btn-primary">
              –°–æ–∑–¥–∞—Ç—å –ø–µ—Ä–≤—É—é –∑–∞—è–≤–∫—É
            </Link>
          )}
        </div>
      ) : (
        <div className="space-y-4">
          {tasks.map(task => (
            <div key={task.id} className="card hover:shadow-md transition-shadow">
              <div className="flex justify-between items-start mb-3">
                <div className="flex-1">
                  <div className="flex items-center gap-3 mb-2">
                    <h3 className="text-lg font-semibold">
                      <Link 
                        to={`/tasks/${task.id}`}
                        className="hover:text-blue-600 transition-colors"
                      >
                        {task.title}
                      </Link>
                    </h3>
                    <span className={statusColors[task.status]}>
                      {statusLabels[task.status]}
                    </span>
                  </div>
                  
                  <div className="text-sm text-gray-600 mb-3">
                    <span className="font-medium">{categoryLabels[task.category]}</span>
                    <span className="mx-2">‚Ä¢</span>
                    <span>üìÖ {formatDate(task.date)}</span>
                    <span className="mx-2">‚Ä¢</span>
                    <span>üïí {formatDateTime(task.createdAt)}</span>
                  </div>
                  
                  <div className="grid grid-cols-1 md:grid-cols-2 gap-4 mb-3">
                    <div>
                      <span className="text-xs text-gray-500">–û—Ç–∫—É–¥–∞:</span>
                      <div className="font-medium">{task.fromAddress}</div>
                    </div>
                    <div>
                      <span className="text-xs text-gray-500">–ö—É–¥–∞:</span>
                      <div className="font-medium">{task.toAddress}</div>
                    </div>
                  </div>
                  
                  <p className="text-gray-700 line-clamp-2">
                    {task.description}
                  </p>
                </div>
                
                <div className="ml-6 text-right">
                  {task.customer && (
                    <div className="flex items-center gap-2 mb-2">
                      <UserAvatar user={task.customer} size="md" />
                      <div className="text-sm">
                        <div className="font-medium">{task.customer.name}</div>
                        {task.customer.role === 'executor' && (
                          <div className="text-gray-500">
                            ‚≠ê {task.customer.rating || '‚Äî'}
                          </div>
                        )}
                      </div>
                    </div>
                  )}
                  
                  <div className="text-sm text-gray-500">
                    –û—Ç–∫–ª–∏–∫–æ–≤: {task.bids?.length || 0}
                  </div>
                </div>
              </div>
              
              <div className="flex justify-between items-center pt-3 border-t">
                <Link 
                  to={`/tasks/${task.id}`}
                  className="text-blue-600 hover:underline text-sm"
                >
                  –ü–æ–¥—Ä–æ–±–Ω–µ–µ ‚Üí
                </Link>
                
                {/* –ö–Ω–æ–ø–∫–∞ –æ—Ç–∫–ª–∏–∫–∞ –∏–ª–∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ —Å—Ç–∞—Ç—É—Å–µ */}
                {user && user.role === 'executor' && task.status === 'active' && user.id !== task.customer?.id && (
                  <div>
                    {task.userBid ? (
                      <div className="text-right">
                        <div className="bg-blue-50 border border-blue-200 rounded px-3 py-2">
                          <div className="flex items-center gap-2 text-blue-800">
                            <span>‚úÖ</span>
                            <div className="text-sm">
                              <div className="font-medium">–í—ã –æ—Ç–∫–ª–∏–∫–Ω—É–ª–∏—Å—å</div>
                              <div className="text-xs">
                                {task.userBid.price} ‚ÇΩ
                                {task.userBid.accepted && (
                                  <span className="ml-1 bg-green-100 text-green-800 px-1 py-0.5 rounded text-xs">
                                    ‚úì –ü—Ä–∏–Ω—è—Ç
                                  </span>
                                )}
                              </div>
                            </div>
                          </div>
                        </div>
                      </div>
                    ) : (
                      <button 
                        onClick={() => handleBidClick(task)}
                        className="btn btn-primary btn-sm"
                      >
                        –û—Ç–∫–ª–∏–∫–Ω—É—Ç—å—Å—è
                      </button>
                    )}
                  </div>
                )}
              </div>
            </div>
          ))}
        </div>
      )}

      {/* –ü–∞–≥–∏–Ω–∞—Ü–∏—è */}
      {pagination.totalPages > 1 && (
        <div className="flex justify-center items-center gap-2 mt-8">
          <button
            onClick={() => handlePageChange(pagination.page - 1)}
            disabled={pagination.page === 1}
            className="btn btn-secondary btn-sm disabled:opacity-50"
          >
            ‚Üê –ù–∞–∑–∞–¥
          </button>
          
          {[...Array(pagination.totalPages)].map((_, i) => {
            const page = i + 1
            return (
              <button
                key={page}
                onClick={() => handlePageChange(page)}
                className={`btn btn-sm ${
                  page === pagination.page 
                    ? 'btn-primary' 
                    : 'btn-secondary'
                }`}
              >
                {page}
              </button>
            )
          })}
          
          <button
            onClick={() => handlePageChange(pagination.page + 1)}
            disabled={pagination.page === pagination.totalPages}
            className="btn btn-secondary btn-sm disabled:opacity-50"
          >
            –í–ø–µ—Ä–µ–¥ ‚Üí
          </button>
        </div>
      )}
      
      {/* –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞—Ö */}
      {tasks.length > 0 && (
        <div className="text-center text-sm text-gray-500 mt-4">
          –ü–æ–∫–∞–∑–∞–Ω–æ {tasks.length} –∏–∑ {pagination.total} –∑–∞—è–≤–æ–∫
        </div>
      )}

      {/* –ú–æ–¥–∞–ª—å–Ω–æ–µ –æ–∫–Ω–æ —Å–æ–∑–¥–∞–Ω–∏—è –æ—Ç–∫–ª–∏–∫–∞ */}
      <BidModal
        isOpen={bidModalOpen}
        onClose={() => {
          setBidModalOpen(false)
          setSelectedTask(null)
        }}
        task={selectedTask}
        onBidCreated={handleBidCreated}
      />
    </div>
  )
}

export default TasksPage 