const userSockets = new Map(); // –•—Ä–∞–Ω–∏–ª–∏—â–µ socket'–æ–≤ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π

io.on('connection', (socket) => {
  console.log('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø–æ–¥–∫–ª—é—á–∏–ª—Å—è:', socket.id);
  
  // –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø—Ä–∏ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–∏
  socket.on('registerUser', (userId) => {
    userSockets.set(userId, socket.id);
    socket.userId = userId;
    console.log(`üë§ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ${userId} –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω —Å socket ${socket.id}`);
  });
  
  socket.on('joinTask', (taskId) => {
    socket.join(`task_${taskId}`);
    console.log(`–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ${socket.id} –ø—Ä–∏—Å–æ–µ–¥–∏–Ω–∏–ª—Å—è –∫ —á–∞—Ç—É –∑–∞–¥–∞—á–∏ ${taskId}`);
  });

  socket.on('leaveTask', (taskId) => {
    socket.leave(`task_${taskId}`);
    console.log(`–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ${socket.id} –ø–æ–∫–∏–Ω—É–ª —á–∞—Ç –∑–∞–¥–∞—á–∏ ${taskId}`);
  });

  socket.on('sendMessage', (data) => {
    socket.to(`task_${data.taskId}`).emit('newMessage', data);
  });

  socket.on('disconnect', () => {
    // –£–¥–∞–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ —Ö—Ä–∞–Ω–∏–ª–∏—â–∞ –ø—Ä–∏ –æ—Ç–∫–ª—é—á–µ–Ω–∏–∏
    if (socket.userId) {
      userSockets.delete(socket.userId);
      console.log(`üë§ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ${socket.userId} –æ—Ç–∫–ª—é—á–∏–ª—Å—è`);
    }
    console.log('–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –æ—Ç–∫–ª—é—á–∏–ª—Å—è:', socket.id);
  });
});

